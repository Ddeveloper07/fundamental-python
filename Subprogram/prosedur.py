# prosedur => sebagai tahap kegiatan untuk menyelesaikan suatu aktivitas.
# prosedur didefinisikan dengan return tanpa ekspresi atau nilai yang dihasilkan di akhir fungsi

# Mendefinisikan dan memanggil prosedur
def greeting(name):
    print("Halo " + name + ", Selamat Datang!")

greeting("Udin Gans")

# Prosedur tanpa parameter input
def salam():
    print("Selamat Pagi Cikgu!")

salam()